#include <stdio.h>
int main() {
    char ch;
    printf("Enter a character: ");
    scanf("%c", &ch);
    if (ch >= 'A' && ch <= 'Z') {
        printf("%c is an uppercase character.\n", ch);
    }
    else if (ch >= 'a' && ch <= 'z') {
        printf("%c is a lowercase character.\n", ch);
    }
    else {
        printf("%c is not a valid alphabetical character.\n", ch);
    }
    return 0;
}
